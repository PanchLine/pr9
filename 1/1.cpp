#include <iostream>
#include <string>

using namespace std;

int main()
{
    setlocale(NULL, "rus");
    string s, x;

    int k = 0;
    cout << "Введите строку: ";
    cin >> s;
    cout << "Введите символ: ";
    cin >> x;
    for (std::string::size_type i = 0; i < s.length(); ++i)
    {
        if (s.find(x, i) == i)
        {
            k++;
        }
    }
    cout << "Количество символов " << x << " в строку " << s << " = " << k << "\n";
    system("pause");
}

